    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Usuario usu = carregarDadosDaTela();
        if (validar()) {
            boolean status = usuDao.salvar(usu);
            if (status) {
                limparCampos();
                preencherTabela();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (jtbUsu.getSelectedRow() != -1) {
            //System.out.println(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 0).toString());
            carregarCamposParaTela();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor selecione um usuário na tabela a baixo para editar!", "Atenção", 1);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (jtbUsu.getSelectedRow() != -1) {
            String usuario = jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 1).toString();

            int option = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o usuário "
                    + usuario + " ?", "Atenção!", JOptionPane.YES_NO_OPTION);
            if (option == 0) {
                //Pegando o conteudo da linha selecionada na coluna 0
                String id = jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 0).toString();
                UsuarioDAO usuDao = new UsuarioDAO();
                usuDao.deletar(Integer.parseInt(id));
                JOptionPane.showMessageDialog(null, "Usuário excluido com sucesso!");
                preencherTabela();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um usuário na tabela abaixo para fazer a exclusão.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

