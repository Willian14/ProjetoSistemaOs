    public void preencherTabela() {
        ArrayList dados = new UsuarioDAO().buscaParaPreencherTabela();
        String[] colunas = {"iduser", "Nome", "Telefone", "Login", "Senha", "Tipo", "Celular"};
        ModeloTabelaUsuarios modelo = new ModeloTabelaUsuarios(dados, colunas);

        jtbUsu.setModel(modelo);

        jtbUsu.getColumnModel().getColumn(0).setPreferredWidth(60);
        jtbUsu.getColumnModel().getColumn(0).setResizable(false);
        jtbUsu.getColumnModel().getColumn(1).setPreferredWidth(250);
        jtbUsu.getColumnModel().getColumn(1).setResizable(false);
        jtbUsu.getColumnModel().getColumn(2).setPreferredWidth(100);
        jtbUsu.getColumnModel().getColumn(2).setResizable(false);
        jtbUsu.getColumnModel().getColumn(3).setPreferredWidth(120);
        jtbUsu.getColumnModel().getColumn(3).setResizable(false);
        jtbUsu.getColumnModel().getColumn(4).setPreferredWidth(120);
        jtbUsu.getColumnModel().getColumn(4).setResizable(false);
        jtbUsu.getColumnModel().getColumn(5).setPreferredWidth(90);
        jtbUsu.getColumnModel().getColumn(5).setResizable(false);
        jtbUsu.getColumnModel().getColumn(6).setPreferredWidth(110);
        jtbUsu.getColumnModel().getColumn(6).setResizable(false);

        jtbUsu.getTableHeader().setReorderingAllowed(false);
        jtbUsu.setAutoResizeMode(jtbUsu.AUTO_RESIZE_OFF);

        jtbUsu.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }


