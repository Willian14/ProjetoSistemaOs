    /**
     * Método que pega todos os valores da linha selecionada na tabela e carrega
     * nos seus respectivos campos.
     */
    public void carregarCamposParaTela() {
        txtNome.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 1).toString());
        jfFone.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 2).toString());
        jfCelular.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 6).toString());
        txtId.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 0).toString());
        txtLogin.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 3).toString());
        txtSenha.setText(jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 4).toString());
        String itemCbx = jtbUsu.getValueAt(jtbUsu.getSelectedRow(), 5).toString();
        for (int i = 0; i < cbxTipo.getItemCount(); i++) {
            if (itemCbx.equals(cbxTipo.getItemAt(i))) {
                cbxTipo.setSelectedIndex(i);
                break;
            }
        }
    }

    /**
     * Método que pega todos os dados digitados nos campos da tela e seta em um
     * objeto Usuario.
     *
     * @return Retorna um Objeto Usuario
     */
    public Usuario carregarDadosDaTela() {
        Usuario usu = new Usuario();
        usu.setNome(txtNome.getText());
        usu.setFone(jfFone.getText());
        usu.setCelular(jfCelular.getText());
        usu.setLogin(txtLogin.getText());
        usu.setSenha(txtSenha.getText());
        usu.setTipo(cbxTipo.getSelectedItem().toString());
        String id = txtId.getText();
        if (!(id.equals(""))) {
            usu.setId(new BigInteger(id));

        }

        return usu;
    }

    public void limparCampos() {
        txtNome.setText(null);
        jfFone.setText(null);
        jfCelular.setText(null);
        txtId.setText(null);
        txtLogin.setText(null);
        txtSenha.setText(null);
        cbxTipo.setSelectedIndex(0);
    }

    public boolean validar() {
        boolean valida = false;
        if (txtNome.getText().equals(null) || txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo nome está em branco! Por favor preencha-o para prosseguir.", "Atenção", 1);
            txtNome.requestFocus();
        } else if (!validaCampoTelefone()) {
            JOptionPane.showMessageDialog(null, "O campo telefone está em branco! Por favor preencha-o para prosseguir.", "Atenção", 1);
            jfFone.requestFocus();
        } else if (!validaCampoCelular()) {
            JOptionPane.showMessageDialog(null, "O campo Celular está em branco! Por favor preencha-o para prosseguir.", "Atenção", 1);
            jfCelular.requestFocus();
        } else if (txtLogin.getText().equals(null) || txtLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo login está em branco! Por favor preencha-o para prosseguir.", "Atenção", 1);
            txtLogin.requestFocus();
        } else if (txtSenha.getText().equals(null) || txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo senha está em branco! Por favor preencha-o para prosseguir.", "Atenção", 1);
            txtSenha.requestFocus();
        } else if (!validaCampoTipo()) {
            JOptionPane.showMessageDialog(null, "Por favor selecione o tipo de usuário para prosseguir.", "Atenção", 1);
            cbxTipo.requestFocus();

        } else {
            valida = true;
        }
        return valida;
    }

    /**
     * *
     * Método que percorre toda a String e verifica se o campo telefone está
     * totalmente preenchido.
     *
     * @return Retorna um valor boolean.
     */
    public boolean validaCampoTelefone() {
        boolean validou = true;
        char c1;
        char c2 = ' ';
        for (int i = 0; i < jfFone.getText().length(); i++) {
            if (i >= 1 && i <= 2) {
                c1 = jfFone.getText().charAt(i);
                if (c1 == c2) {
                    validou = false;
                }
            } else if (i >= 5 && i <= 8) {
                c1 = jfFone.getText().charAt(i);
                if (c1 == c2) {
                    validou = false;
                }
            } else if (i >= 10 && i <= 13) {
                c1 = jfFone.getText().charAt(i);
                if (c1 == c2) {
                    validou = false;
                } else {
                    return validou;
                }
            }
        }
        return validou;
    }

